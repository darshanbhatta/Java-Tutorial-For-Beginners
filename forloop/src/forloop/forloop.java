package forloop;

public class forloop {

	public static void main(String[] args) {

		//prints out all odd numbers from 0 1000 using for loops
		for(int i =0;i<=1000;i++) {

			if(i%2==1) {
				System.out.print(i + " , ");
			}

		}



	}
}

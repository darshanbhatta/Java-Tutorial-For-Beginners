public class datatype {

	public static void main(String[] args) {
	
	//the main data types you need to know are int, doubles, booleans, chars and Strings
		
		
	//creating an initializing a byte data type
	byte b = 127;
	System.out.println(b);
	
	//creating an initializing a byte data type
	short s = -9999;
	System.out.println(s);
	
	//creating an initializing a byte data type
	int i = 999999;
	System.out.println(i);
	
	//creating an initializing a long data type
	long l = 999999999;
	System.out.println(l);

	//creating an initializing a float data type
	float f = 5.5f;
	System.out.println(f);

	//creating an initializing a double data type
	double d = 999;
	System.out.println(d);
	
	//creating an initializing a boolean data type
	boolean bo = true;
	System.out.println(bo);

	//creating an initializing a char data type
	char c = 'q';
	System.out.println(c);
	
	//creating an initializing a String
	String st = "wjeajksdjsajkldjdjaljsdjkaskldajls";
	System.out.println(st);
	

	}
}

package whileloop;

public class whileloo {

	public static void main(String[] args) {

		int i = 5;

		//example of a while loop, this one prints out numbers from 5 to 1
		while(i>0) {

			System.out.println(i);
			i--;
		}

		i=5;

		//example of a do while loop, this one prints out numbers from 4 to 0
		do {
			i--;
			System.out.println(i);

		}while(i>0);

	}

}
